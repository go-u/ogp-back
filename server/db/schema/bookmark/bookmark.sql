CREATE TABLE bookmark
(
  id INT UNSIGNED NOT NULL AUTO_INCREMENT UNIQUE,
  user_id INT UNSIGNED NOT NULL,
  ogp_id INT UNSIGNED NOT NULL,
  created_at DATETIME NOT NULL,

  PRIMARY KEY (id),
    UNIQUE (user_id, ogp_id)
);

